package main

import (
	"bytes"
	"context"
	"fmt"
	"os"
	"strings"

	"github.com/cloudinary/cloudinary-go/v2"
	"github.com/cloudinary/cloudinary-go/v2/api/uploader"
	"github.com/joho/godotenv"
)

func main() {
	godotenv.Load(".env")
	cloudinaryURL := strings.TrimSpace(os.Getenv("CLOUDINARY_URL"))
	fmt.Printf("CLOUDINARY_URL = %q\n", cloudinaryURL)

	cld, err := cloudinary.NewFromURL(cloudinaryURL)
	if err != nil {
		fmt.Printf("PARSE ERROR: %v\n", err)
		return
	}
	fmt.Println("URL parsed OK — attempting test upload...")

	// 1×1 white JPEG
	jpegBytes := []byte{
		0xFF, 0xD8, 0xFF, 0xE0, 0x00, 0x10, 0x4A, 0x46, 0x49, 0x46, 0x00, 0x01,
		0x01, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0xFF, 0xDB, 0x00, 0x43,
		0x00, 0x08, 0x06, 0x06, 0x07, 0x06, 0x05, 0x08, 0x07, 0x07, 0x07, 0x09,
		0x09, 0x08, 0x0A, 0x0C, 0x14, 0x0D, 0x0C, 0x0B, 0x0B, 0x0C, 0x19, 0x12,
		0x13, 0x0F, 0x14, 0x1D, 0x1A, 0x1F, 0x1E, 0x1D, 0x1A, 0x1C, 0x1C, 0x20,
		0x24, 0x2E, 0x27, 0x20, 0x22, 0x2C, 0x23, 0x1C, 0x1C, 0x28, 0x37, 0x29,
		0x2C, 0x30, 0x31, 0x34, 0x34, 0x34, 0x1F, 0x27, 0x39, 0x3D, 0x38, 0x32,
		0x3C, 0x2E, 0x33, 0x34, 0x32, 0xFF, 0xC0, 0x00, 0x0B, 0x08, 0x00, 0x01,
		0x00, 0x01, 0x01, 0x01, 0x11, 0x00, 0xFF, 0xC4, 0x00, 0x1F, 0x00, 0x00,
		0x01, 0x05, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08,
		0x09, 0x0A, 0x0B, 0xFF, 0xDA, 0x00, 0x08, 0x01, 0x01, 0x00, 0x00, 0x3F,
		0x00, 0xFB, 0xD6, 0xFF, 0xD9,
	}

	resp, err := cld.Upload.Upload(context.Background(), bytes.NewReader(jpegBytes), uploader.UploadParams{
		Folder:   "guards-test",
		PublicID: "copilot_credential_test",
	})
	if err != nil {
		fmt.Printf("SDK Go error: %v\n", err)
		return
	}

	fmt.Printf("resp.Error.Message = %q\n", resp.Error.Message)
	fmt.Printf("resp.SecureURL     = %q\n", resp.SecureURL)
	fmt.Printf("resp.PublicID      = %q\n", resp.PublicID)

	if resp.Error.Message != "" {
		fmt.Println("\n❌ Cloudinary rejected the upload — credentials are wrong.")
		fmt.Println("   Fix your .env CLOUDINARY_URL:")
		fmt.Println("   cloudinary://API_KEY:API_SECRET@CLOUD_NAME")
		fmt.Println("   API_KEY   = numeric (e.g. 868432573914261)")
		fmt.Println("   API_SECRET = alphanumeric (the long secret from Cloudinary Console)")
		fmt.Println("   Go to: https://console.cloudinary.com/settings/api-keys")
	} else {
		fmt.Println("\n✅ Upload succeeded! Credentials are valid.")
	}
}
